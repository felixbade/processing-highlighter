window.onload = function() {
    var elements = document.getElementsByClassName('processing-code');
    for (var i = 0; i < elements.length; i++) {
        var plain_code = elements[i].innerHTML;
        //var words = plain_code.match(/[A-Za-z][A-Za-z0-9_]*[^A-Za-z]*/gi);
        var words = plain_code.match(/(([A-Za-z_][A-Za-z0-9_]*|\n)([^A-Za-z_\/]|\/[^A-Za-z_\/])*|\/\/[^\n]*)/gi);

        for (var j = 0; j < words.length; j++) {
            var a_word = words[j].match(/^[A-Za-z_][A-Za-z0-9_]*/g);
            if (! (a_word != null && a_word !== undefined)) {
                console.log(words[j]);
                if (words[j].lastIndexOf('//') === 0) {
                    words[j] = '<span class="processing-code-comment1">' + words[j] + '</span>';
                }
            } else {
                var word = words[j].match(/[A-Za-z_][A-Za-z0-9_]*/g)[0];
                for (var k = 0; k < keywords.length; k++) {
                    var key = keywords[k][0];
                    var type = keywords[k][1];
                    if (word === key) {
                        var tail = words[j].substring(word.length, words[j].length);
                        var is_a_function = tail.lastIndexOf('(') === 0;
                        var should_be_a_function = type.lastIndexOf('function') === 0;
                        if (is_a_function !== should_be_a_function) {
                            continue;
                        }

                        var head = word.replace(key, '<span class="processing-code-' + type + '">' + key + '</span>');
                        words[j] = head + tail;
                        break;
                    }
                }
                console.log(words[j]);
            }
        }
        elements[i].innerHTML = words.join('');
    }
}

